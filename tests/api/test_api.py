import pytest
import requests
import json
import time


BASE = "https://jsonplaceholder.typicode.com"


# ── Helpers ───────────────────────────────────────────────────────────────────

def validate_schema(data: dict, required_keys: list):
    missing = [k for k in required_keys if k not in data]
    assert not missing, f"Schema violation — missing keys: {missing}"


# ── GET Requests ──────────────────────────────────────────────────────────────

@pytest.mark.api
def test_get_single_user():
    """Valid GET — returns user with correct schema."""
    r = requests.get(f"{BASE}/users/1", timeout=10)
    assert r.status_code == 200
    data = r.json()
    validate_schema(data, ["id", "name", "username", "email", "address", "phone"])
    assert data["username"] == "Bret"
    assert data["email"] != ""


@pytest.mark.api
def test_get_non_existent_user():
    """Negative GET — 404 for non-existent resource."""
    r = requests.get(f"{BASE}/users/99999", timeout=10)
    assert r.status_code == 404


