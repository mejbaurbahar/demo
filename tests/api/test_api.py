# ── Resilience ────────────────────────────────────────────────────────────────

@pytest.mark.api
@pytest.mark.reliability
def test_api_resilience_timeout():
    """Resilience: Verifies the requests library handles timeout gracefully."""
    try:
        requests.get(f"{BASE}/posts", timeout=0.001)
        assert True  # If it completes within 1ms, that's fine
    except requests.exceptions.Timeout:
        assert True  # Expected — demonstrates resilience handling
    except Exception:
        assert True  # Any network error is acceptable here


@pytest.mark.api
@pytest.mark.reliability
def test_api_retry_simulation():
    """Reliability: Simulates a retry pattern on transient failure."""
    max_retries = 3
    for attempt in range(max_retries):
        r = requests.get(f"{BASE}/posts/1", timeout=10)
        if r.status_code == 200:
            break
        time.sleep(0.5 * (attempt + 1))  # Exponential-ish back-off
    assert r.status_code == 200, f"API did not succeed after {max_retries} retries."


