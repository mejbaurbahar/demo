# ── POST Requests ─────────────────────────────────────────────────────────────

@pytest.mark.api
def test_create_post_valid(store_payload):
    """Valid POST — creates resource and validates echo response."""
    payload = {"title": "Autonomous AI QA Test", "body": "Generated by AI engine", "userId": 1}
    store_payload(payload)
    r = requests.post(f"{BASE}/posts", json=payload, timeout=10)
    assert r.status_code == 201
    data = r.json()
    validate_schema(data, ["id", "title", "body", "userId"])
    assert data["title"] == payload["title"]


